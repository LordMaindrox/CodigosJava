    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed
        this.num2 = Double.parseDouble(this.resultado.getText());
        this.visor.setText(this.visor.getText()+this.resultado.getText());
        switch (this.operador){
            case "+" :
                this.resultado.setText(sinDecimal(this.num1+this.num2));
                break;
            case "-" :
                this.resultado.setText(sinDecimal(this.num1-this.num2));
                break;
            case "X" :
                this.resultado.setText(sinDecimal(this.num1*this.num2));
                break;
            case "/": 
                if (num2 == 0){
                    this.resultado.setText("Error al dividir con cero");
                }else{
                    this.resultado.setText(sinDecimal(this.num1/this.num2));
                }
                break;
            case "pow":
                this.visor.setText(" Elevado a la " +this.resultado.getText() + " da: ");
                this.resultado.setText(String.valueOf(Math.pow(num1,num2)));
                break;
        }
    }//GEN-LAST:event_btn_igualActionPerformed

