    public String sinDecimal(Double resultado){
        String quitarDecimal = "";
        quitarDecimal = Double.toString(resultado);
        if (resultado%1 == 0){
            quitarDecimal = quitarDecimal.substring(0,quitarDecimal.length()-2);
        }
        
        return quitarDecimal;
    
}
    
